func countCharacters(words []string, chars string) int {
	dict := make([][26]int, len(words))
	cnt := 0

	for _, c := range chars {
		for i := 0; i < len(words); i++ {
			dict[i][c-'a']++
		}
	}

	for idx, word := range words {
		bingo := true
		for _, wc := range word {
			if dict[idx][wc-'a'] != 0 {
				dict[idx][wc-'a']--
			} else {
				bingo = false
				break
			}
		}

		if bingo {
			cnt += len(word)
		}
	}

	return cnt
}
